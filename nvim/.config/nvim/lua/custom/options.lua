-- Suas configurações de tabulação
vim.opt.expandtab = true    -- Usar espaços em vez de tabs
vim.opt.tabstop = 2         -- Um tab equivale a 2 espaços
vim.opt.softtabstop = 2     -- Tamanho do tab ao pressionar a tecla Tab
vim.opt.shiftwidth = 2      -- Tamanho do recuo (indentação)

-- Desabilitar arquivos de swap (evita os arquivos .swp)
vim.opt.swapfile = false

-- Mostrar números de linha
vim.opt.number = true
vim.opt.relativenumber = true

-- Destaque na linha atual
vim.opt.cursorline = true
